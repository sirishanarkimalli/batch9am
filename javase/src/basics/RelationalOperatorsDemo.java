package basics;

public class RelationalOperatorsDemo {

	public static void main(String[] args) {
		int a = 24, b = 26, c = 24;

		System.out.println("a equalto b= "+ (a == b));
		System.out.println(a > b);
		System.out.println(a < b);
		System.out.println(a >= b);
		System.out.println(a <= c);
		System.out.println(a != b);

	}

}
